<div class="constitution">
    <h1 bind:this={one}>SHIVA DYNASTY FOOTBALL LEAGUE CONSTITUTION</h1>

    <div class="subBlock">
        <p>This constitution outlines the rules and regulations for the Shiva Dynasty Football League. All managers are expected to adhere to these guidelines to ensure a fair and competitive environment.</p>
    </div>

    <h2 class="sectionHeading" bind:this={two}>1. Governing Body</h2>
    <div class="subBlock">
        <ul>
            <li><strong>A.</strong> Jayden Gatica is Commissioner.</li>
            <li><strong>B.</strong> Frankie Frein is the Co-Commissioner TO THE Commissioner.</li>
            <li><strong>C.</strong> Spencer Farrell is the assistant TO THE co-commissioner TO THE Commissioner.</li>
            <li><strong>D.</strong> These three make up the Shiva Governing Committee (SGC). This committee breaks ties on league votes, writes new rules, and decides on league issues.</li>
            <li><strong>E.</strong> All rule changes require a majority vote. Ties are broken by the SGC.</li>
            <li><strong>F.</strong> The commissioner runs day-to-day operations and interprets the appropriate course of action based on this Constitution and Sleeper’s guidelines.</li>
        </ul>
    </div>

    <h2 class="sectionHeading" bind:this={three}>2. League Members and Conduct</h2>
    <div class="subBlock">
        <ul>
            <li><strong>A.</strong> There will be 10 league members. Expansion requires a league vote.</li>
            <li><strong>B.</strong> Managers have the right to retain their franchise unless banned by the SGC.</li>
            <li><strong>C.</strong> Bannings may occur due to negligence, intentional tanking, or failure to pay fees.</li>
            <li><strong>D.</strong> Vacant spots are filled by referral first, then randoms, subject to a league vote.</li>
            <li><strong>E.</strong> Abandoned teams won't be offered to existing members without a majority vote.</li>
            <li><strong>F.</strong> Owners must be active participants. There is no offseason in dynasty!</li>
            <li><strong>G. Activity Expectations:</strong> 
                <ol>
                    <li>Setting valid lineups.</li>
                    <li>Responding to trades in a timely manner.</li>
                    <li>Participating in league proposals and votes.</li>
                </ol>
            </li>
        </ul>
    </div>

    <h2 class="sectionHeading" bind:this={four}>3. Tanking Policy</h2>
    <div class="subBlock">
        <p><strong>A. Prohibited Tanking:</strong> Tanking to affect competitiveness is not tolerated.</p>
        <ul>
            <li>1. You may NOT intentionally start players on bye or injured.</li>
            <li>2. <strong>Exception:</strong> You are not forced to drop a valuable bench stash for a one-week filler (K/DEF/etc). Communicate this with the Commish before kickoff.</li>
            <li>3. You may NOT sit "stud" players for "dud" players to lose on purpose.</li>
        </ul>
        <p><strong>B. Legal Tanking:</strong> You may "tank" by trading away productive veterans for future picks or young prospects, provided you start your best available lineup each week.</p>
    </div>

    <h2 class="sectionHeading" bind:this={five}>4. League Setup</h2>
    <div class="subBlock">
        <p><strong>A. League Fees:</strong> Current entry fee is ${dues}. Fees are due prior to the rookie draft. Failure to pay results in draft pick penalties or an automatic ban.</p>
        <p><strong>B. Rosters:</strong> 28 active spots, 5 taxi spots, and 5 IR spots. Lineup changes require a majority vote and a two-season "grace period" before implementation.</p>
        <p><strong>C. Scoring:</strong> 1.0 PPR. Full rules are available in the Sleeper settings.</p>
    </div>

    <h2 class="sectionHeading" bind:this={six}>5. Taxi Squads</h2>
    <div class="subBlock">
        <ul>
            <li><strong>A.</strong> Up to 5 spots for taxi-eligible players.</li>
            <li><strong>B.</strong> Eligible for 1st-year (rookie) players. Must be activated or dropped by their 4th year.</li>
            <li><strong>C.</strong> 3rd-year players must be moved to the active roster by 5/31 of the off-season entering their 4th year, or they will be automatically cut on 6/1.</li>
        </ul>
    </div>

    <h2 class="sectionHeading" bind:this={seven}>6. Draft</h2>
    <div class="subBlock">
        <ul>
            <li><strong>1. Order:</strong> Non-playoff teams are determined by the Draft Points Formula. Ties are broken by potential points, then points scored.</li>
            <li><strong>2. Playoffs:</strong> 1st–4th place draft based on finish (1st place picks last).</li>
            <li><strong>3. Consolation:</strong> Losers of the 1st round play for the 7th and 8th picks.</li>
            <li><strong>4. Format:</strong> 5-round "Standard" (non-snake) slow draft (8-hour clock, pauses 11PM-8AM CT).</li>
            <li><strong>5. Roster Management:</strong> Final drops for the draft must occur by May 31st.</li>
        </ul>
    </div>

    <h2 class="sectionHeading" bind:this={eight}>7. Post Draft Period</h2>
    <div class="subBlock">
        <p>Free agency opens immediately after the draft. Position maximums are: 10 QBs, 5 Ks, and 5 D/STs.</p>
    </div>

    <h2 class="sectionHeading" bind:this={nine}>8. Waivers</h2>
    <div class="subBlock">
        <p><strong>A. FAAB:</strong> Blind bidding system. Tiebreaker is reverse standings.</p>
        <p><strong>B. Locking:</strong> Free agents lock after Week 16 until the June rookie draft.</p>
        <p><strong>C. Roster Churning:</strong> Managers cannot pick up/drop more than 5 players at a single position in one week to block others. Doing so results in public shaming and manual player unlocking.</p>
    </div>

    <h2 class="sectionHeading" bind:this={ten}>9. Trades</h2>
    <div class="subBlock">
        <ul>
            <li><strong>A. Deadline:</strong> Week 13. Trades resume after Week 18.</li>
            <li><strong>B. Vetoes:</strong> Only allowed if collusion is suspected (requires 7 votes).</li>
            <li><strong>C. Future Picks:</strong> Trading future picks requires paying 50% of that season's dues in advance.</li>
            <li><strong>D. Ethics:</strong> "Don't be a dick/stupid." Pull trades if a player gets hurt; don't snap-accept if you know a player just got injured.</li>
        </ul>
    </div>

    <h2 class="sectionHeading" bind:this={eleven}>10. Regular Season & Playoffs</h2>
    <div class="subBlock">
        <p><strong>Regular Season:</strong> 14 weeks. Three-strike policy for invalid lineups. 3rd strike results in a ban.</p>
        <p><strong>Playoffs:</strong> Weeks 15-18. Top 2 seeds get byes. Championship round is a two-week aggregate (Weeks 17 & 18).</p>
    </div>

    <h2 class="sectionHeading" bind:this={twelve}>11. Payouts</h2>
    <div class="subBlock">
        <ul>
            <li><strong>1st Place:</strong> $450.00</li>
            <li><strong>2nd Place:</strong> $50.00</li>
        </ul>
    </div>

    <h2 class="sectionHeading" bind:this={thirteen}>12. Amendments</h2>
    <div class="subBlock">
        <p>Requires majority approval. Mid-season changes apply to the following year. Amendments are named after the member who identified the need for the rule.</p>
    </div>
</div>
